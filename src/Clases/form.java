    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
  String nombre = txtNombre.getText();
String nickname = txtNickname.getText();
String clave = new String(txtClave.getPassword());

if (!nombre.isEmpty() && !nickname.isEmpty() && !clave.isEmpty()) {
    Clases.Usuarios.guardarUsuario(nombre, nickname, clave);
    JOptionPane.showMessageDialog(this, "Usuario guardado exitosamente.");
    txtNombre.setText("");
    txtNickname.setText("");
    txtClave.setText("");
} else {
    JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos.", "Error", JOptionPane.ERROR_MESSAGE);
}


    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnListarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnListarActionPerformed
    txtArea.setText(""); // Limpiar el Ã¡rea antes de listar los usuarios

    for (Usuarios u : Usuarios.listaUsuarios) { // Acceder a la lista de usuarios
        txtArea.append("Nombre: " + u.getNombre() + "\n");
        txtArea.append("Nickname: " + u.getNickname() + "\n");
        txtArea.append("Clave: " + u.getClave() + "\n");
        txtArea.append("-------------------------\n");
    }

    if (Usuarios.listaUsuarios.isEmpty()) {
        txtArea.setText("No hay usuarios registrados.");
    }

    }//GEN-LAST:event_btnListarActionPerformed

